# Grounding. Systems must understand the concepts they reason over and operate with.
# We refer to this capacity as grounding. Grounding allows AI system to demonstrate connection between its outputs
# and the abstract concepts that they operate with. It will also enable systems to understand their risks and limitations.
# Such an improved conceptual understanding should also lead to robust AI that adapt gracefully and quickly to new domains,
# is robust to surprise, and resists malicious manipulation.